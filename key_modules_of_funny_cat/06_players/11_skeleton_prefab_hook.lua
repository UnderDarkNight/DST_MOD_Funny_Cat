-- 先让玩家不管什么模式都掉落骨架
    local player_common_extensions = require("prefabs/player_common_extensions")
    local old_OnPlayerDeath = player_common_extensions.OnPlayerDeath
    player_common_extensions.OnPlayerDeath = function(inst, data)
        data = data or {}
        data.skeleton = true

        inst.skeleton_prefab = "funny_cat_other_skeleton"

        old_OnPlayerDeath(inst, data)
    end
